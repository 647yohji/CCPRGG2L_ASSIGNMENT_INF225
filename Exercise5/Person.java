public class Person {

    private String name;

    public String getName() {
        return this.name;
    }

    public void setName(String newName) {
        this.name = newName;
    }
    
    private int ccNum;
    
    public int getCCNum() {
        return this.ccNum;
    }

    public void setCCNum(int newCCNum) {
        this.ccNum = newCCNum;
    }
}

